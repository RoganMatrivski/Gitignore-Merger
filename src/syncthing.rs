#[cfg(test)]
mod tests {
    use std::path::PathBuf;

    use super::*;
    use crate::gitignore::{PrefixedRule, Rule};

    fn make_rule(pattern: &str, negated: bool, dir_only: bool, rel: &str) -> PrefixedRule {
        PrefixedRule {
            rule: Rule {
                pattern: pattern.into(),
                negated,
                dir_only,
            },
            relative_dir: PathBuf::from(rel),
        }
    }

    #[test]
    fn simple_pattern_gets_prefixed() {
        let r = make_rule("*.log", false, false, "backend");
        assert_eq!(prefixed_rule_to_stignore(&r).unwrap(), "/backend/*.log");
    }

    #[test]
    fn negation_preserved() {
        let r = make_rule("keep.log", true, false, "backend");
        assert_eq!(prefixed_rule_to_stignore(&r).unwrap(), "!/backend/keep.log");
    }

    #[test]
    fn root_level_rule_no_extra_slash() {
        let r = make_rule("*.tmp", false, false, "");
        // No prefix → no leading slash added
        assert_eq!(prefixed_rule_to_stignore(&r).unwrap(), "*.tmp");
    }

    #[test]
    fn dir_only_trailing_slash_stripped() {
        // gitignore `build/` → stignore `build` (no trailing slash)
        let r = make_rule("build", false, true, "frontend");
        let line = prefixed_rule_to_stignore(&r).unwrap();
        assert!(!line.ends_with('/'), "stignore lines must not end with '/'");
    }

    #[test]
    fn already_anchored_pattern() {
        // gitignore `/dist` inside `apps/web` → `/apps/web/dist`
        let r = make_rule("/dist", false, false, "apps/web");
        assert_eq!(prefixed_rule_to_stignore(&r).unwrap(), "/apps/web/dist");
    }

    #[test]
    fn double_star_preserved() {
        let r = make_rule("**/node_modules", false, false, "packages/ui");
        assert_eq!(
            prefixed_rule_to_stignore(&r).unwrap(),
            "/packages/ui/**/node_modules"
        );
    }
}
