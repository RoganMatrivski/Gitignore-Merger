/// Syncthing `.stignore` format conversion.
///
/// Key differences from `.gitignore`:
/// - Comments use `//` instead of `#`
/// - No trailing `/` to mark directories; Syncthing matches both files and
///   dirs with the same pattern
/// - Leading `/` anchors to the folder root (same semantics as gitignore)
/// - `**` works the same way
/// - `!` prefix for negation (same)
/// - `(?i)` prefix makes the pattern case-insensitive
/// - `(?d)` prefix means "delete ignored items" (opt-in)
///
/// Reference: https://docs.syncthing.net/users/ignoring.html
use crate::gitignore::PrefixedRule;

// ---------------------------------------------------------------------------
// Rule conversion
// ---------------------------------------------------------------------------

/// Convert a single [`PrefixedRule`] into a Syncthing `.stignore` line.
///
/// Returns `None` for rules that have no meaningful stignore representation.
pub fn prefixed_rule_to_stignore(r: &PrefixedRule) -> Option<String> {
    let prefix = r.relative_dir.to_string_lossy();
    let stripped = r.rule.pattern.trim_start_matches('/');

    // Build the path-prefixed pattern
    let pattern = if prefix.is_empty() {
        stripped.to_string()
    } else {
        format!("{}/{}", prefix, stripped)
    };

    if pattern.is_empty() {
        return None;
    }

    let mut line = String::new();

    // Negation comes first, before any Syncthing modifier flags
    if r.rule.negated {
        line.push('!');
    }

    // Anchor with `/` whenever we have a concrete sub-directory prefix so
    // the rule only matches inside that subtree, not anywhere in the repo.
    if !prefix.is_empty() || r.rule.pattern.starts_with('/') {
        if !line.ends_with('!') {
            // Negation already written; the `/` goes right after it
        }
        line.push('/');
    }

    line.push_str(&pattern);

    // Syncthing does not use trailing `/` for dir-only matching, so we omit it.
    // If you want to restrict to directories only you'd need `(?d)` semantics,
    // but that changes deletion behaviour — we leave that decision to the user.

    Some(line)
}

// ---------------------------------------------------------------------------
// Document-level helpers
// ---------------------------------------------------------------------------

/// Section header emitted at the top of every generated `.stignore`.
const HEADER: &str = "\
// .stignore — auto-generated by gitignore-merge
// DO NOT EDIT: regenerate with `gitignore-merge --format syncthing`
//
// Syncthing ignore pattern reference:
//   https://docs.syncthing.net/users/ignoring.html
";

/// Merge a slice of [`PrefixedRule`]s into a `.stignore` document string.
pub fn merge_to_stignore(rules: &[PrefixedRule]) -> String {
    let body: Vec<String> = rules.iter().filter_map(prefixed_rule_to_stignore).collect();

    if body.is_empty() {
        return format!("{}\n// (no rules found)\n", HEADER);
    }

    format!("{}\n{}\n", HEADER, body.join("\n"))
}

